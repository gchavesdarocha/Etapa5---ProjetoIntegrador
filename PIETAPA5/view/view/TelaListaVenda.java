    public void atualizarTabelaGeral() {
        if (!atendente.vendas.isEmpty()) {
            atualizarTabela(atendente.vendas);
        } else if (!gerente.vendas.isEmpty()) {
            atualizarTabela(gerente.vendas);
        } else {
            tabelaModelo = new DefaultTableModel(tableColumns, 0);
            tblVendas.setModel(tabelaModelo);
        }
    }

    public void atualizarTabela(List<Venda> vendas) {
        Venda venda;

        tabelaModelo = new DefaultTableModel(tableColumns, 0);

        for (int i = 0; i < vendas.size(); i++) {

            venda = vendas.get(i);

            String[] rowData = {String.valueOf(venda.getId()), String.valueOf(venda.getValorTotal())};

            tabelaModelo.addRow(rowData);
        }

        tblVendas.setModel(tabelaModelo);
    }
